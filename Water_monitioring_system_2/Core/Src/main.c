	  //Tính độ pH
	  pH_Current_Val = ((pH_ADC_Val / 4095.0) * 16.0) + 4.0; // 0 - 16 mA --> 4 - 20 mA
	  pH = (pH_Current_Val - 4)/16 * 14; //Chuyển đổi từ dòng sang giá trị pH

	  modbus_t *ctx = modbus_new_rtu(RS485_UART_HANDLE.hdmarx->Instance, 9600, 'N', 8, 1);
	  Modbus_ReadRegisters(ctx, MODBUS_SLAVE1_ADDRESS, modbusData1);
	  Modbus_ReadRegisters(ctx, MODBUS_SLAVE2_ADDRESS, modbusData2);

	  HAL_Delay(500);

	  data[0] = pH;l
	  data[1] = temp;
	  data[2] = modbusData1;
	  data[3] = modbusData2;

	  SendData(4, &data);

	  HAL_Delay(SampleTime);



